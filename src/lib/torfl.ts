export const LEVELS: Level[] = TORFL;
export const ALL_TASKS: ProductionTask[] = TORFL.flatMap((l) => l.tasks);

export function findLevel(cefr: string): Level | undefined {
  return TORFL.find((l) => l.cefr === cefr);
}

export function findTask(id: string): ProductionTask | undefined {
  return ALL_TASKS.find((t) => t.id === id);
}

/** A level is validated when all its 6 épreuves have been passed. */
export function levelValidated(level: Level, passed: Set<string>): boolean {
  return level.tasks.every((t) => passed.has(t.id));
}

/** Target description for the LLM (the official level — no sub-levels). */
export function targetDescription(task: ProductionTask): string {
  return `${task.cefr}`;
}

