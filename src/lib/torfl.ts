// The 5 official ТРКИ/TORFL subtests.
export type Skill =
  | "grammaire" // Лексика-Грамматика — QCM lexique/grammaire
  | "lecture" // Чтение — compréhension écrite (texte ru + questions)
  | "ecoute" // Аудирование — compréhension orale (texte ru lu à voix haute + questions)
  | "ecrit" // Письмо — expression écrite
  | "oral"; // Говорение — expression orale

export interface ProductionTask {
  id: string; // e.g. "A1-ecrit"
  cefr: Cefr;
  skill: Skill;
  titleFr: string;
  promptFr: string; // instruction in French; the response is in Russian (production)
  minWords?: number;
  generated?: boolean; // true = content (text/questions/QCM) is generated by the LLM
}

export interface McqQuestion {
  question: string; // in Russian (a sentence with a gap or a grammar choice)
  options: string[]; // candidate answers
}

/** An exam item generated on demand for the generated subtests. */
export interface ExamItem {
  passage?: string; // lecture / ecoute: a Russian text
  questions?: string[]; // lecture / ecoute: questions in French
  mcq?: McqQuestion[]; // grammaire: multiple-choice questions
  token?: string; // grammaire: server-side key to the correct answers (never the answers themselves)
}

export interface Level {
  cefr: Cefr;
  band: Band;
  title: string; // official designation
  subtitle: string;
  theme: string; // short theme hint used when generating items
  tasks: ProductionTask[];
}

// Build the 6 épreuves of a level: two authored production tasks (écrit + oral) plus four
// generated tasks (compréhension écrite/orale, traduction ru→fr / fr→ru).
function tasks(
  cefr: Cefr,
  ecrit: { titleFr: string; promptFr: string; minWords: number },
  oral: { titleFr: string; promptFr: string },
): ProductionTask[] {
  // Official ТРКИ order: Лексика-Грамматика, Чтение, Аудирование, Письмо, Говорение.
  return [
    {
      id: `${cefr}-grammaire`,
      cefr,
      skill: "grammaire",
      generated: true,
      titleFr: "Lexique-grammaire",
      promptFr: "Choisis la bonne réponse pour chaque question (QCM).",
    },
    {
      id: `${cefr}-lecture`,
      cefr,
      skill: "lecture",
      generated: true,
      titleFr: "Compréhension écrite",
      promptFr: "Lis le texte russe, puis réponds aux questions en français.",
    },
    {
      id: `${cefr}-ecoute`,
      cefr,
      skill: "ecoute",
      generated: true,
      titleFr: "Compréhension orale",
      promptFr: "Écoute le texte russe (bouton ▶), puis réponds aux questions en français.",
    },
    { id: `${cefr}-ecrit`, cefr, skill: "ecrit", ...ecrit },
    { id: `${cefr}-oral`, cefr, skill: "oral", ...oral },
  ];
}

