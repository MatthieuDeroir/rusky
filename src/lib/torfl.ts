// TORFL (ТРКИ) validation track. The official exam has exactly 6 CEFR levels (A1→C2), grouped
// into 3 bands (A/B/C). There are NO official sub-levels (A1.1 etc. are only language-school
// course units), so we stick to the 6 real levels. Progress is validated step by step through
// the 6 épreuves of each level. v1 covers production (écrit/oral) + comprehension (écrite/orale)
// + translation (ru↔fr), all graded by an LLM (see lib/mistral). Pure data + helpers (no deps).

export type Cefr = "A1" | "A2" | "B1" | "B2" | "C1" | "C2";
export type Band = "A" | "B" | "C";

